    private void billModelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_billModelMouseClicked
        int row = billModel.getSelectedRow();
        String masp = billModel.getValueAt(row, 0).toString();
        if(selectedProducts.contains(masp)){
            selectedProducts.remove(masp);
        }
        else{
            selectedProducts.add(masp);
        }
        double totalPrice=0.0;
        for(String selectedProduct : selectedProducts){
            for(int i =0;i<Model2.getRowCount();i++){
                if(selectedProduct.equals(Model2.getValueAt(i, 0).toString())){
                    double gia = Double.parseDouble(Model2.getValueAt(i, 2).toString());
                    totalPrice+=gia ;
                    break;
                }
            }
        }
        sotien.setText(String.valueOf(totalPrice)  + "đ" );

    }//GEN-LAST:event_billModelMouseClicked

