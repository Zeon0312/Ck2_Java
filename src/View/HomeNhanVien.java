    public void confirmPayment(){
        int option = JOptionPane.showConfirmDialog(this,"Xác nhận thanh toán","Xác nhận",JOptionPane.YES_NO_OPTION);
        if(option == JOptionPane.YES_OPTION){
            JOptionPane.showMessageDialog(this," Thanh toán thành công!");
            
           
            for(int i = selectedProducts.size()-1;i>=0;i--){
                String masp = selectedProducts.get(i);
                for(int j=0;j<Model2.getRowCount();j++){
                    if(masp.equals(Model2.getValueAt(j, 0).toString())){
                        Model2.removeRow(j);
                        break;
                    }
                }
                 deleteSelectedItemDatabase();
            }
//            selectedProducts.clear();
            sotien.setText("0 đ");
        }
    }
    
    
